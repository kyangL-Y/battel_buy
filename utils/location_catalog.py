def match_standard_province(text: str | None) -> str | None:
    normalized = _normalize_text(text)
    if not normalized:
        return None
    for alias in PROVINCE_ALIASES_SORTED:
        if alias and alias in normalized:
            return PROVINCE_ALIASES[alias]
    return None


def match_standard_city(text: str | None) -> tuple[str | None, str | None]:
    normalized = _normalize_text(text)
    if not normalized:
        return None, None
    for alias in CITY_ALIASES_SORTED:
        if alias and alias in normalized:
            canonical_city = CITY_CANONICAL_NAMES.get(alias, alias)
            return canonical_city, CITY_TO_PROVINCE[alias]
    return None, None
